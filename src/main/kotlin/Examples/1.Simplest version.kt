/**
 * We declare a package-level function main which returns Unit
 * 我们声明一个返回Unit的包级别的main函数，
 * and takes an Array of strings as a parameter.
 * 函数采用字符串数组作为参数。
 * Note that semicolons are optional.
 * 注意，分号是可选的。
 */

fun main(args: Array<String>) {
    println("Hello, world!")
    println("嗨，你好！")
}

